const map = input.split("\n").map((line) =>
    line.split("").map((char) => ({
        content: char,
        visited: false,
    }))
);

input = null;

// the order simulates turning right from the current direction
const directionOffsets = [
    { x: 0, y: -1 }, // up
    { x: 1, y: 0 }, // right
    { x: 0, y: 1 }, // down
    { x: -1, y: 0 }, // left
];

// find the starting position
let y = 0;
let x = 0;
let found = false;
for (y = 0; y < map.length; y++) {
    for (x = 0; x < map[y].length; x++) {
        if (map[y][x].content === "^") {
            found = true;
            break;
        }
    }
    if (found) {
        break;
    }
}

// verify
if (map[y][x].content !== "^") {
    throw new Error("Starting position not found");
}

// part 1 - trace the guard's path
let direction = 0;

// mark the starting position as visited
map[y][x].visited = true;
let uniquePositions = []; // needed for part 2

const start = {
    y,
    x,
    direction,
};

let step = start;
while (true) {
    step = nextPosition(step);

    // we're done if we leave the map
    if (!step) break;

    if (!map[step.y][step.x].visited) {
        uniquePositions.push({ x: step.x, y: step.y });
    }

    map[step.y][step.x].visited = true;
}

// the +1 accounts for the starting position of the guard.
// for part 2, we cannot test an obstacle at the guard's starting position
console.log(`unique positions: ${uniquePositions.length + 1}`);

// part 2 - obstacles that create loops
let obstacleCounts = 0;
while (uniquePositions.length > 0) {
    const { x: obsX, y: obsY } = uniquePositions.shift();

    map[obsY][obsX].content = "#"; // insert the obstacle

    let fast = start;
    let slow = start;
    let moveSlow = 3;
    const steps = [];
    while (true) {
        fast = nextPosition(fast);

        if (fast === null) {
            // the guard left the map so there is no loop
            break;
        }

        // it's not a loop unless the fast pointer catches
        // the slow pointer going in the same direction
        if (
            fast.x === slow.x &&
            fast.y === slow.y &&
            slow.direction === fast.direction
        ) {
            // we found a loop
            obstacleCounts++;
            break;
        }

        steps.push(fast);
        moveSlow--;
        if (moveSlow === 0) {
            slow = steps.shift();
            moveSlow = 2;
        }
    }

    map[obsY][obsX].content = "."; // remove the obstacle
}

console.log(`obstacles that create loops: ${obstacleCounts}`);

function nextPosition(currentStep) {
    const within = (p, min, max) => p >= min && p <= max;

    let x = currentStep.x;
    let y = currentStep.y;
    let direction = currentStep.direction;

    while (true) {
        const yNext = y + directionOffsets[direction].y;
        const xNext = x + directionOffsets[direction].x;

        // we're done if we leave the map
        if (
            !within(yNext, 0, map.length - 1) ||
            !within(xNext, 0, map[y].length - 1)
        ) {
            return null;
        }

        if (map[yNext][xNext].content === "#") {
            // turn right and recalculate
            direction = (direction + 1) % 4;
        } else {
            return {
                y: yNext,
                x: xNext,
                direction: direction,
            };
        }
    }
}
